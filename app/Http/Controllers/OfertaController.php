     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $oferta=new Oferta;
        $oferta->tipo_oferta_id=$request->tipo_oferta_id;
        $oferta->porcentaje_descuento=$request->porcentaje_descuento;
        $oferta->fecha_inicio=$request->fecha_inicio;
        $oferta->fecha_fin=$request->fecha_fin;
        $oferta->save();

        return response($oferta,200);
    }

